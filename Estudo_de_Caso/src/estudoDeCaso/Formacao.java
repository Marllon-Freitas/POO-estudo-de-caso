package estudoDeCaso;

public class Formacao extends Instrutor {
	//Variáveis
	private Integer id;
	private String nome;
	
	//Métodos
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
